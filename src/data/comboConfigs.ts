export function getComboId(planetIds: string[]): string | null {
  const sorted = [...planetIds].sort()
  const key = sorted.join('-')
  if (comboConfigs[key]) return key
  const reversed = [...sorted].reverse().join('-')
  if (comboConfigs[reversed]) return reversed
  return null
}

export function getComboConfig(planetIds: string[]): ComboConfig | null {
  const comboId = getComboId(planetIds)
  return comboId ? comboConfigs[comboId] : null
}

